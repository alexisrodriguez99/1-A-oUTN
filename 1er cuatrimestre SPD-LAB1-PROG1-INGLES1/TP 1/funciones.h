/** \brief funcion que pide un numero
 *
 * \param recibe el mensaje que le pase
 * \return devuelve un numero ingresado por el usuario
 *
 */
float getFloat(char mensaje[]);
/** \brief funcion que realiza una suma
 *
 * \param recibe el primer numero
 * \param recibe el segundo numero
 * \return devuelve el resultado de la suma
 *
 */
float sumar(float numero1, float numero2);
/** \brief funcion que realiza una resta
 *
 * \param recibe el primer numero
 * \param recibe el segundo numero
 * \return devuelve el resultado de la resta
 *
 */
float restar(float numero1, float numero2);
/** \brief funcion que realiza una multiplicacion
 *
 * \param recibe el primer numero
 * \param recibe el segundo numero
 * \return devuelve el resultado de la multiplicacion
 *
 */
float multiplicar(float numero1, float numero2);
/** \brief funcion que realiza una division
 *
 * \param recibe el primer numero
 * \param recibe el segundo numero
 * \return devuelve el resultado de la division
 *
 */
float dividir(float numero1, float numero2);
/** \brief funcion que realiza el factorial de una numero
 *
 * \param recibe un numero
 * \return devuelve el resultado del factorial de un numero
 *
 */
int factorial(float numero);

